               <h3 class="subtitle">まとめ</h3>

               いかがでしょうか？
                <br><br>
                ご案内のようにネット証券を選ぶポイントは皆さんそれぞれかと思います。幸いにも口座開設は無料かつキャンペーンを開催している証券会社もありますので、気になる証券会社があればぜひ口座開設をおすすめします。
                
