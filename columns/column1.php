            株式投資を始めるための基礎知識として、まずは株とはどういうものなのかを説明します。結論からお話しすると株とはズバリ、会社の権利を小分けにしたものなのです。
            <br><br>
            そしてその権利を持つということは、その会社のオーナー（株主）になるということです。でも、これだけではイメージがわきませんよね。ここから具体的に説明していきます。
            <br><br>
            まず会社を作り事業を始める際に必要なものは資金なのです。そこで、資金を集める、言い換えると自分の会社に投資をしてくれる人を探す必要があります。その際に、資金を出してくれた人に会社の権利の一部を対価として渡すのです。そうです、この会社の権利こそが株式なのです。.315％の税金がかかります。申告分離課税とは、給料収入などのほかの所得とは切り離して、年間の株式の利益から税金を計算し確定申告をする課税制度のことです。
            <br><br>
            そして、株式投資つまり株を買うということは、その会社の権利を買うということです。私たちは、会社に資金を出すことによって、その会社の一部を自分のものにすることができます。
            <br><br>
            つまり、オーナーになるということです。このようにして多くの人から集めた資金を使って会社は事業を行います。だから、会社というのは社長の物ではありません。会社に資金を出してくれた、株主の物なのです。
            <br><br>
            では、会社の権利とは何があるのでしょうか？まず、会社のオーナーになるということなので、会社の経営に参加することができるのです。原則として、年に1回株主総会という、簡単に言えばオーナーの集まりがあります。これは会社の最高の意思決定の会議なのです。
            <br><br>
            ここでは、自分の意見を言ったり、重要な決議の投票に参加したりすることができます。そして、もう一つは会社の利益を受け取る権利です。株式投資をする方の多くはこの権利のために投資している人が多いですが、その会社の事業が上手く行き、利益が出た場合は、保有している株式の数に応じて利益を分配することになります。
