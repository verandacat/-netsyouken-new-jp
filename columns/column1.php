            そのほかの株主の権利として、株主優待があります。これは会社から株主に対して、自社製品や優待券などを提供するものです。例えば、食品会社の株主優待には、食料品のセットがあったり、映画館を運営している会社であれば、映画を一回無料で見ることのできる優待券がもらえたりします。これは、会社側からの株主に対する感謝の気持ちです。現在は、株主優待をまとめた書籍まで販売され、株式投資の目的の一つになっています。
            <br><br>
            さて、ここまでは株の説明として、株式は会社の権利の一部であること、そして、その権利とは会社の経営に参加できること、また、会社の利益を受け取れること、株主優待をもらえることだと説明しました。では、よく聞く「株で儲けた。株で損した。」というのはどういう意味なのでしょうか？ここからは株の売買についてお話ししたいと思います。
            <br><br>
            これまで、株とはどういうものなのか。また、株主の権利についてお話ししました。では、株の売買とは一体何をすることなのでしょうか。ズバリ、文字通り株を買ったり売ったりするということです。例えば、Aさんが○○商事という会社の株を1000株持っているとします。Bさんは○○商事という株が欲しいと思っています。
            <br><br>
            この時、AさんはBさんに株を売ることができるのです。Aさんが1株500円なら売っても良い、Bさんも1株500円なら買いたいとなった時に、1株500円で1000株なので、○○の株を50万円でBに渡すのです。ここで考えてみてください。
            <br><br>
            もしもAさんが○○商事の株を1株400円で買っていたとしたら、Aさんは40万円で買った株を50万円で売ったことになります。つまり、10万円を株の売買で儲けたことになるのです。これこそが「株で儲けた。」のカラクリなのです。反対に買った金額よりも安い金額で売ってしまえば、「株で損した。」ということになるのです。すごく単純な話なのです。
            <br><br>
            では、この株の値段（これを株価といいます）はどのようにして変動するのでしょうか。皆さんに考えてほしいのですが、オークションでは、買いたいという人が多ければ多いほど値段は上がっていきますよね。逆に売りたいという人が多ければ値段は下がっていきます。
            <br><br>
            株も同じなのです。その株を買いたい人が多ければ、株価は上がりますし、売りたい人が多ければ株価は下がります。このようにして、株価は日々変わっていくのです。利益の増えた会社の株は買いたい人が多くなり株価は上がっていきますし、逆に利益が減った会社の株は、売りたい人が多くなり株価は下がっていきます。
            <br><br>
            要するに株価は、買いたいという人と売りたい人のバランスで決まっているということです。だから、株を買うときはこれからみんなが買いたいと思うような会社の株を買うことができれば、将来的に株価が上がり、利益を出すことができるのです。
            <br><br>
            まずは、株とは何か。株を買うということはどういうことなのか。株を買うとどんな権利が得られるのか。そして、「株で儲けた、損した。」とはどういうことなのかを、株式投資を始める前に理解しておきましょう。基本の原理原則はすごく簡単なことです。
