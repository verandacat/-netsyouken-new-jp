    <h3>NISAとは</h3>
    <section class="sub-text-box">
        <p>
            株や投資信託の売却益や配当には、通常およそ約20%の税金がかかります。NISAとは2014年にスタートした少額投資非課税制度のことでNISA口座内で購入した金融商品に対する利益が非課税となる制度です。
            <br><br>
            もともとは英国のISA(Individual Savings Account訳:個人貯蓄口座) の制度を手本としたもので、個人の投資促進のためにつくられた制度です。
            <br><br>
            NISA口座をつくると、2023年までは毎年1人当たり120万円までのNISA口座の非課税枠で株や投資信託を購入することが出来ます。
            <br><br>
            NISA口座で購入した株や投資信託は最長5年間の非課税期間が設定されます。5年を超えて保有する場合は新たに設定される年のNISA枠に移管するか、通常の課税口座に移管するかを選ぶことが出来ます。
            <br><br>
            そのほか2016年から始まった未成年者を対象としたジュニアNISAの制度があります。0歳から19歳までの未成年者を対象にNISAと同じようにジュニアNISA口座で購入した株や投資信託に非課税枠が設定されるというものです。ジュニアNISAの毎年の非課税枠は80万円までとなっています。
            <br><br>
            さらに2018年からは長期での資産形成を目的としたつみたてNISAがスタートしました。非課税期間が20年と長く、年間40万円までの積み立て投資に適用できます。注意点はつみたてNISAでは株を買えないため投資信託だけの取り扱いとなります。また、通常のNISAとの併用はできません。
            <br><br>
            NISAの口座は1人1口座であり、複数の証券会社にまたがってNISA口座を開設することは出来ません。NISA口座を作るためにはまず通常の証券口座を開設する必要があります。NISA口座の開設には証券会社にもよりますが、通常の証券口座よりも時間がかかるため、NISA口座での投資を考えている方は早めの手続きをお勧めします。
